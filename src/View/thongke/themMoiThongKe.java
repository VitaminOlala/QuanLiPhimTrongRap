    boolean ktraMaPhim(String ma) {
        ThongKeBusiness thuchien = new ThongKeBusiness();
        List<ThongTinThongKe> l = thuchien.layDanhSach();
        for (int i = 0; i < l.size(); i++) {
            if (ma.equals(l.get(i).getMaPhim())) {
                return true;
            }
        }
        return false;
    }
    private void hienThiChiTietThongKe() {
        //Khai báo 1 đối tuong để sử dụng thông tin của lớp đấy
        ThongKeBusiness thuchien = new ThongKeBusiness();
        //Lấy thông tin chỉ tiết của phòng chiếu
        ThongTinThongKe objtk = thuchien.layChiTiet(maBoPhim);
        jTextField1.setText(maBoPhim);
        jTextField1.setEnabled(rootPaneCheckingEnabled);
        if (objtk != null) {
            //Hiển thị lên giao diện
            jTextField2.setText(objtk.getTenPhim());
            jTextField3.setText(objtk.getSoBuoiChieu());
            jTextField4.setText(objtk.getSoVeBanRa());
            jTextField5.setText(objtk.getTongDoanhThu());


        }
    }    
