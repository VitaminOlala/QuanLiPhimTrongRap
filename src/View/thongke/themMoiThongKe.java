        ThongTinThongKe objtk = new ThongTinThongKe();
        ThongKeBusiness thuchien = new ThongKeBusiness();

        //ghi thông tin 
        String maPhim = jTextField1.getText();

        objtk.setMaPhim(jTextField1.getText());
        objtk.setTenPhim(jTextField2.getText());
        objtk.setSoBuoiChieu(jTextField3.getText());
        objtk.setSoVeBanRa(jTextField4.getText());
        objtk.setTongDoanhThu(jTextField5.getText());


        boolean kq = false;
        if (!maPhim.isEmpty()) {
            if (ktraMaPhim(maPhim)) { //TH Sửa
                System.out.println("da cos trong db");
                thuchien.capNhatThongKe(objtk);
                kq = true;
            } else {
                thuchien.themMoiThongKe(objtk);
                kq = true;

            }
            if (kq != false) {

                //Reloa lại danh sách
                JOptionPane.showMessageDialog(rootPane, "Cập nhật thành công.");

                HeThongBaoCao.hienThiDanhSachThongKe();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập mã phòng chiếu");
        }        
